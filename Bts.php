      <p> <b>PHYSIQUE CHIMIE </b><br>
        • Dosages volumétriques, potentiométriques, conductimétriques.<br>
        • Analyses biochimiques.<br>
        • Techniques chromatographiques (CCM, HPLC, CPG).<br>
        • Techniques électrophorétiques.<br>
        • Techniques Spectrophotométriques.</p>
      <p> <b>BIOLOGIE</b><br>
        • Culture in vitro d’explants et de protoplastes.<br>
        • Histologie.<br>
        • Culture cellulaire animale.<br>
        • Enzymologie.<br>
        • Parasitologie.<br>
        • Immunologie.</p>
      <p> <b>MICROBIOLOGIE</b><br>
        • Techniques microbiologiques de base.<br>
        • Observations microscopiques.<br>
        • Identification bactérienne (techniques de base et techniques rapides d’identification).<br>
        • Diagnostic dans les domaines médicaux, vétérinaire, en hygiène alimentaire.<br>
        • Génétique bactérienne. <br>
        • Biologie moléculaire (PCR).<br>
        • Réalisation de bioréactions.</p>
      <p><b>MODULES GÉNÉRAUX</b><br>
        • Expression.<br>
        • Qualité.<br>
        • Economie.<br>
        • Statistiques.<br>
        • Éducation Socioculturelle.<br>
        • Anglais.<br>
        • Education Physique et Sportive (EPS).<br>
        • Accompagnement au projet personnel et professionel.</p>
