
<h1 class="titreformation">BTS Analyses Biologiques <br>et Biotechnologiques (Anabiotech)</h1>

    <div class="divForm">
      <p > Le BTSA Anabiotec est une formation supérieure courte à caractère professionnel, qui permet de bien s'insérer dans la vie active. L'enseignement général permet de développer des facultés de compréhension du milieu socioprofessionnel, ainsi que l'aptitude à la communication et au traitement de l'information.</p>
